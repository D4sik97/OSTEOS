def set_user_lang(user_id, lang, full_name=None):
    """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç —è–∑—ã–∫ –∏ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤–æ –≤—Ä–µ–º–µ–Ω–Ω–æ–π –ø–∞–º—è—Ç–∏."""
    if user_id not in user_state:
        user_state[user_id] = {}
    
    user_state[user_id]["lang"] = lang
    if full_name:
        user_state[user_id]["full_name"] = full_name
    logging.info(f"User {user_id} set lang to {lang}. State: {user_state[user_id]}")


